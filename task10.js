/* 
Write a JavaScript code.
Given n as input, print on the console Good if n>=75, Average,
if n is greater than equal to 50 but less than 75 and Poor otherwise. 
Take n=63 and output expected is Average 
*/
const n = 63;
if (n >= 75) {
  console.log('Good');
} else if (n >= 50) {
  console.log('Average');
} else {
  console.log('Poor');
}
