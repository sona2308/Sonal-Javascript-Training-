/*
Write a JavaScript code. Given 2 numbers m and n. 
If m is greater than n, display Larger in the console. 
If they are equal display Equal. 
If m is less than n, display Smaller. 
Take  m=10, n=3 and output expected is Larger. 
*/
let m = 10;
let n = 3;
if (m > n) {
  console.log("Larger");
} else if (m < n) {
  console.log("Smaller");
} else {
  console.log("Equal");
}
